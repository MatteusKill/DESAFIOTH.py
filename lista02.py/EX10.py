import math

numeroDigitado = int(input("Digite um numero inteiro: "))

if numeroDigitado > 0:
    quadradoDoNumero = numeroDigitado * 2
    print(f"O numero digitado ao quadrado: {quadradoDoNumero}.")
    raizDoNumero = math.sqrt(numeroDigitado)
    print(f"16A raiz quadrada do numero digitado eh: {raizDoNumero}.")